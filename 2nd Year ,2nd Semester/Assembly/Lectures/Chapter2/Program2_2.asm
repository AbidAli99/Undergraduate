; Program2-1, purpose: adds 4 words of data
.MODEL SMALL
.STACK 64
.DATA

DATA_IN	DW 234DH,1DE6H, 3BC7H, 566AH
SUM		DW	0000H

.CODE
MAIN PROC FAR
; INITIALIZE DATA SEGMENT
	MOV	AX, @DATA
	MOV	DS, AX

	MOV CX, 04H
	MOV DI, OFFSET DATA_IN
	MOV BX, 00H
ADD_LP: ADD BX, [DI]
	INC DI
	INC DI
	DEC CX
	JNZ ADD_LP
	MOV SI, OFFSET SUM
	MOV [SI], BX

; RETURN TO DOS
	MOV AX, 4C00H
	INT 21H
MAIN ENDP
END MAIN