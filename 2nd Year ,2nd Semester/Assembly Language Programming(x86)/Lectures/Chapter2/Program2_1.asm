; Program2-1, purpose: adds 5 bytes of data
.MODEL SMALL
.STACK 64
.DATA

DATA_IN	DB 25H, 12H,15H,1FH,2BH
SUM		DB	00H

.CODE
MAIN PROC FAR
; INITIALIZE DATA SEGMENT
	MOV	AX, @DATA
	MOV	DS, AX

	MOV CX, 05H
	MOV BX, OFFSET DATA_IN
	MOV AL, 0
AGAIN: ADD AL, [BX]
	INC BX
	DEC CX
	JNZ AGAIN
	MOV SUM, AL

; RETURN TO DOS
	MOV AX, 4C00H
	INT 21H
MAIN ENDP
END MAIN